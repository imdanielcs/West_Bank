       private void mostrarBienvenida() {
        System.out.println("Rut:" + this.cliente.getRut());
        String rut = this.cliente.getRut();
        Connection conexion = Conexion.obtenerConexion();
        if (conexion == null) {
            JOptionPane.showMessageDialog(this, "Error al conectar con la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            // Consulta SQL para obtener los datos del cliente por su rut
            String consulta = "SELECT * FROM cliente WHERE rut = ?";
            try (PreparedStatement preparedStatement = conexion.prepareStatement(consulta)) {
                preparedStatement.setString(1, rut);

                // Ejecutar la consulta
                try (ResultSet resultSet = preparedStatement.executeQuery()) {
                    // Verificar si se encontr칩 un cliente
                    if (resultSet.next()) {
                        String nombre = resultSet.getString("nombre");
                        String apellido_paterno = resultSet.getString("apellido_paterno");
                        String apellido_materno = resultSet.getString("apellido_materno");
                        // Mostrar los datos en la interfaz gr치fica
                        lblBienvenido.setText("Bienvenido(a): " + nombre + " " + apellido_paterno + " " + apellido_materno);
                        lblSaldo.setText("Saldo: " + resultSet.getString("saldo"));
                    } else {
                        JOptionPane.showMessageDialog(this, "Cliente no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }

        }   catch (SQLException ex) {
                    ex.printStackTrace();
        }
        
    }
        
